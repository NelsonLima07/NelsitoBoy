#ifndef INC_USR_CORES_RGB565_H_
#define INC_USR_CORES_RGB565_H_

// Básicas
#define COLOR_BLACK        0x0000  // Preto
#define COLOR_WHITE        0xFFFF  // Branco
#define COLOR_RED          0xF800  // Vermelho
#define COLOR_GREEN        0x07E0  // Verde
#define COLOR_BLUE         0x001F  // Azul
#define COLOR_CYAN         0x07FF  // Ciano
#define COLOR_MAGENTA      0xF81F  // Magenta
#define COLOR_YELLOW       0xFFE0  // Amarelo
#define COLOR_ORANGE       0xFC00  // Laranja

// Tons adicionais
#define COLOR_LIGHT_BLUE   0x7D7C  // Azul Claro
#define COLOR_DARK_BLUE    0x0010  // Azul Escuro
#define COLOR_LIGHT_GREEN  0x87F0  // Verde Claro
#define COLOR_DARK_GREEN   0x03E0  // Verde Escuro
#define COLOR_LIGHT_RED    0xF81C  // Vermelho Claro
#define COLOR_DARK_RED     0x7800  // Vermelho Escuro
#define COLOR_PINK         0xF97F  // Rosa
#define COLOR_PURPLE       0x780F  // Roxo

// Tons de Cinza
#define COLOR_GRAY         0x8410  // Cinza
#define COLOR_LIGHT_GRAY   0xC618  // Cinza Claro
#define COLOR_DARK_GRAY    0x4208  // Cinza Escuro

// Outros
#define COLOR_BROWN        0x8A22  // Marrom
#define COLOR_GOLD         0xFEA0  // Dourado
#define COLOR_SILVER       0xC618  // Prateado
#define COLOR_SKY_BLUE     0x867F  // Azul Céu
#define COLOR_TEAL         0x0410  // Verde-azulado
#define COLOR_BEIGE        0xD6BA  // Bege
#define COLOR_MAROON       0x7800  // Bordô
#define COLOR_OLIVE        0x7BE0  // Verde Oliva

#endif /* INC_USR_CORES_RGB565_H_ */
